public class EgyszeruFuggvenyek
{
    public static int faktorialis(int n){
        
        int i = 1;
        
        int faktorialis = 1;
        
        while (i < n)
        {
            i++;
            faktorialis = faktorialis * i;
        }
        return faktorialis;
    }
}
    
   
        
  
        
        
    
    


 

    
    
    
    
    
    
    
    
    
 



        
    
